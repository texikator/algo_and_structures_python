# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др.
# над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.

print(f'Binary AND: {5 & 6}')
print(f'Binary OR: {5 | 6}')
print(f'Binary XOR: {5 ^ 6}')
print(f'Binary Ones Complement for 5: {~5}')
print(f'Binary ones complement for 6: {~6}')
print(f'Binary shift left: {5 << 2}')
print(f'Binary shift right {5 >> 2}')